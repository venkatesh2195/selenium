package dec28;
public class Using_Forloop {
	public static void main(String[] args) {
		for (int i=1;i<=10;i++)
		{
			System.out.println("Venkatesh::"+i);
		}
		System.out.println("===============");
		for(int i=1;i<10;i+=1)
		{
			System.out.println("Venkatesh::"+i);	
		}
		System.out.println("============");
		for(int i=20;i>=1;i--)
		{
			System.out.println("Venkatesh::"+i);
		}

	}


}
